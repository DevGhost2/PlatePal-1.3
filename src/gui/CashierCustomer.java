    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked

        int sr = jTable2.getSelectedRow();

        cr = null;
        cu = CashierCustomerUpdate.getinstance().getinstance();

        HashMap<String, String> map = new HashMap();

        if (evt.getClickCount() == 2) {

            String mobile = String.valueOf(jTable2.getValueAt(sr, 4));

            try {

                ResultSet rs = MySQL2.executeSearch("SELECT * FROM `customer` INNER JOIN `gender` ON `customer`.`gender_id`=`gender`.`id` WHERE `mobile`='" + mobile + "' ");

                if (rs.next()) {

                    cu.userCanUpdateHisType = true;
                    map.put("id", rs.getString("customer_id"));
                    map.put("registered_date", rs.getString("registered_date"));
                    map.put("gender", rs.getString("gender.type"));
                    map.put("emp", rs.getString("employee_id"));
                    map.put("email", rs.getString("email"));
                    map.put("mobile", rs.getString("mobile"));
                    map.put("fn", rs.getString("first_name"));
                    map.put("ln", rs.getString("last_name"));

                    cu.setData(map);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

            cu.setVisible(true);

        }


    }//GEN-LAST:event_jTable2MouseClicked


