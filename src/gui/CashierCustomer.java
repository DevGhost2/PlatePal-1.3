    private static void loadCustomers() {
        try {

            String query = "SELECT * FROM `customer` INNER JOIN `gender` ON `customer`.`gender_id`=`gender`.`id`";
            String order = String.valueOf(jComboBox1.getSelectedItem());

            String keyWord = jTextField1.getText();

            if (!keyWord.isEmpty()) {
                query += " WHERE `customer`.`id` LIKE '" + keyWord + "%' OR `first_name` LIKE '" + keyWord + "%' OR `last_name` LIKE '" + keyWord + "%' OR `email` LIKE '" + keyWord + "%' OR `mobile` LIKE '" + keyWord + "%' OR `gender`.`type` LIKE '" + keyWord + "%'";
            }

            if (order.equals("Order By")) {
                query += " ORDER BY `customer`.`id` ASC";
            } else if (order.equals("ID ASC")) {
                query += " ORDER BY `customer`.`id` ASC";
            } else if (order.equals("ID DSC")) {
                query += " ORDER BY `customer`.`id` DESC";
            } else if (order.equals("First Name")) {
                query += " ORDER BY `first_name` ASC";
            } else if (order.equals("Last Name")) {
                query += " ORDER BY `last_name` ASC";
            } else if (order.equals("Mobile")) {
                query += " ORDER BY `mobile` ASC";
            }

            //System.out.println(query);
            ResultSet resultSet = MySQL2.executeSearch(query);
            DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();
            tableModel.setRowCount(0);

            while (resultSet.next()) {
                Vector vector = new Vector();
                vector.add(resultSet.getString("customer_id"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("registered_date"));
                vector.add(resultSet.getString("gender.type"));

                tableModel.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

