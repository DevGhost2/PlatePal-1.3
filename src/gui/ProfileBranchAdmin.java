     private void loadEmployeeProfile() {
        try {
            // Assuming you have the logged-in employee's email stored somewhere
            // For now, let's just get the first employee as an example
            ResultSet resultset = MySQL2.executeSearch(
                "SELECT e.*, b.name AS branch_name " +
                "FROM employee e " +
                "INNER JOIN branch b ON e.branch_id = b.id " +
                "LIMIT 1");
            
            if (resultset.next()) {
                FirstNameField.setText(resultset.getString("first_name"));
                LastNameField.setText(resultset.getString("last_name"));
                MobileNumberField.setText(resultset.getString("mobile"));
                EmployeeIDField.setText(resultset.getString("emp_id"));
                BranchNameField.setText(resultset.getString("branch_name"));
                EmailField.setText(resultset.getString("email"));
                PasswordField.setText(resultset.getString("password"));
                
                // Make ID and Branch fields non-editable
                EmployeeIDField.setEditable(false);
                BranchNameField.setEditable(false);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, 
                "Error loading profile data: " + e.getMessage(),
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }

