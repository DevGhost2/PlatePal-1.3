    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String firstName = FirstNameField.getText().trim();
        String lastName = LastNameField.getText().trim();
        String mobile = MobileNumberField.getText().trim();
        String email = EmailField.getText().trim();
        String password = PasswordField.getText().trim();
        String employeeId = EmployeeIDField.getText().trim();
        
        // Validate the inputs
        if (firstName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "First name cannot be empty", "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (lastName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Last name cannot be empty", "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mobile number cannot be empty", "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Invalid mobile number format. Should be like 07XXXXXXXX", "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email cannot be empty", "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9_-]+(\\.[A-Za-z0-9_-]+)*@[^-][A-Za-z0-9-]+(\\.[A-Za-z0-9-]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Invalid email format", "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password cannot be empty", "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            // Update the employee in the database
            String updateQuery = "UPDATE employee SET "
                    + "first_name = '" + firstName + "', "
                    + "last_name = '" + lastName + "', "
                    + "mobile = '" + mobile + "', "
                    + "email = '" + email + "', "
                    + "password = '" + password + "' "
                    + "WHERE emp_id = '" + employeeId + "'";
            
            MySQL2.executeIUD(updateQuery);
            
            JOptionPane.showMessageDialog(this, "Profile updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            
            // Disable editing and show Edit button
            FirstNameField.setEditable(false);
            LastNameField.setEditable(false);
            MobileNumberField.setEditable(false);
            EmailField.setEditable(false);
            PasswordField.setEditable(false);
            
            jButton2.setVisible(true);
            jButton3.setVisible(false);
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, 
                "Error updating profile: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

