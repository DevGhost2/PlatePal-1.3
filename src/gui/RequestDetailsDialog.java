    private void loadRequestDetails() {
    try {
        String query = "SELECT er.*, CONCAT(e.first_name, ' ', e.last_name) AS employee_name, " +
                       "e.emp_id, e.mobile, e.email " +
                       "FROM employee_requests er " +
                       "INNER JOIN employee e ON er.employee_id = e.id " +
                       "WHERE er.request_id = '" + requestId + "'";
        
        System.out.println("Executing query: " + query); // Debug line
        
        ResultSet rs = MySQL2.executeSearch(query);
        
        if (rs.next()) {
            // Debug: Print all available columns
            System.out.println("Found request data:");
            System.out.println("Employee Name: " + rs.getString("employee_name"));
            System.out.println("Request ID: " + rs.getString("request_id"));
            System.out.println("Request Type: " + rs.getString("request_type"));
            
            // Set the form fields using your component names - with null checks
            String employeeName = rs.getString("employee_name");
            EmployeeNameLoading.setText(employeeName != null ? employeeName : "N/A");
            
            String requestIdFromDB = rs.getString("request_id");
            RequestIdLoading.setText(requestIdFromDB != null ? requestIdFromDB : "N/A");
            
            String empId = rs.getString("emp_id");
            EmployeeIDLoading.setText(empId != null ? empId : "N/A");
            
            String requestType = rs.getString("request_type");
            RequestTypeLoading.setText(requestType != null ? requestType : "N/A");
            
            String dateSubmitted = rs.getString("date_submitted");
            DateSubmittedLoading.setText(dateSubmitted != null ? dateSubmitted : "N/A");
            
            currentStatus = rs.getString("status");
            CurrentStatusLoading.setText(currentStatus != null ? currentStatus : "Pending");
            
            // Set request details in the text field
            String description = rs.getString("description");
            RequestDetailsLoading.setText(description != null ? description : "");
            
            // Enable/disable buttons based on current status
            if (currentStatus != null && (currentStatus.equals("Approved") || currentStatus.equals("Rejected"))) {
                ApproveButton.setEnabled(false);
                RejectButton.setEnabled(false);
                RequestDetailsLoading.setEditable(false);
            }
        } else {
            System.out.println("No request found with ID: " + requestId);
            JOptionPane.showMessageDialog(this, 
                "No request found with ID: " + requestId + "\n" +
                "Make sure the employee_requests table exists and has sample data.",
                "Request Not Found", 
                JOptionPane.WARNING_MESSAGE);
        }
        
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, 
            "Error loading request details: " + e.getMessage() + "\n" +
            "This might be because the employee_requests table doesn't exist yet.\n" +
            "Please create the table and add sample data first.",
            "Database Error", 
            JOptionPane.ERROR_MESSAGE);
    }
}


