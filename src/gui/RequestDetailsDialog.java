    private void updateRequestStatus(String status) {
    String adminResponse = RequestDetailsLoading.getText().trim();
    
    if (adminResponse.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Please provide details/reason before " + status.toLowerCase() + " the request.",
            "Response Required", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    try {
        // Update the request status in database
        String updateQuery = "UPDATE employee_requests SET " +
                            "status = '" + status + "', " +
                            "admin_response = '" + adminResponse + "', " +
                            "response_date = NOW(), " +
                            "admin_id = 1 " +  // You might want to get the actual admin ID
                            "WHERE request_id = '" + requestId + "'";
        
        MySQL2.executeIUD(updateQuery);
        
        JOptionPane.showMessageDialog(this, 
            "Request has been " + status.toLowerCase() + " successfully!",
            "Success", 
            JOptionPane.INFORMATION_MESSAGE);
        
        // Update UI
        CurrentStatusLoading.setText(status);
        ApproveButton.setEnabled(false);
        RejectButton.setEnabled(false);
        RequestDetailsLoading.setEditable(false);
        
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, 
            "Error updating request: " + e.getMessage(),
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    }
}

