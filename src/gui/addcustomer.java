    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (checkRandNumber()) {

            int id = rand;
            String customer_id = "CUST" + (rand);

            String fname = jTextField2.getText();
            String lname = jTextField3.getText();
            String email = jTextField4.getText();
            String mobile = jTextField5.getText();
            String gender = String.valueOf(jComboBox1.getSelectedItem());
            String employee = String.valueOf(jComboBox2.getSelectedItem());

            if (customer_id.isEmpty()) {

                JOptionPane.showMessageDialog(this, "pleses enter your customer id", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (fname.isEmpty()) {

                JOptionPane.showMessageDialog(this, "pleses enter your frist name", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (lname.isEmpty()) {

                JOptionPane.showMessageDialog(this, "pleses enter your last name", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (email.isEmpty()) {

                JOptionPane.showMessageDialog(this, "pleses enter your email", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9_-]+(\\.[A-Za-z0-9_-]+)*@[^-][A-Za-z0-9-]+(\\.[A-Za-z0-9-]+)*(\\.[A-Za-z]{2,})$")) {
                JOptionPane.showMessageDialog(this, "Email address invalid, please try again.", "warning", JOptionPane.WARNING_MESSAGE);
            } else if (mobile.isEmpty()) {

                JOptionPane.showMessageDialog(this, "pleses select your mobile ", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {

                JOptionPane.showMessageDialog(this, "your phone number is invalid", "warning", JOptionPane.WARNING_MESSAGE);

            } else if (gender.equals("Select")) {

                JOptionPane.showMessageDialog(this, "pleses select your gender ", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (employee.equals("Select")) {

                JOptionPane.showMessageDialog(this, "pleses select your employee name ", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                LocalDateTime now = LocalDateTime.now();
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
                String formattedDate = now.format(formatter);

                int genderid = GenderMap.get(gender);
                int employeeid = employeeMap.get(employee);

                try {

                    ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `customer` WHERE `mobile` = '" + mobile + "' ");

                    if (resultSet.next()) {
                        JOptionPane.showMessageDialog(this, "YOU ARE ALREADY REGISTED");

                    } else {

                        MySQL2.executeIUD("INSERT INTO `customer` (`id`,`customer_id`,`first_name`,`last_name`,`email`,`mobile`,`registered_date`,`gender_id`,`employee_id`) VALUE ('" + id + "','" + customer_id + "','" + fname + "','" + lname + "','" + email + "','" + mobile + "','" + formattedDate + "','" + genderid + "','" + employeeid + "')");

                        JOptionPane.showMessageDialog(this, "YOU ARE REJISTED");
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }

        } else {
            checkRandNumber();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    public boolean checkRandNumber() {

        boolean value = true;

        System.out.println(rand);
        try {
            ResultSet rs = MySQL2.executeSearch("SELECT * FROM `customer` WHERE `id`='" + rand + "'");

            if (rs.next()) {
                rand = new Random().nextInt(10000);
                value = false;

            } else {
                value = true;
            }

        } catch (Exception e) {
        }

        return value;

    }

