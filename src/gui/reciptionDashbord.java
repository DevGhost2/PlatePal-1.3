    private void loadDashboardData() {
    try {
        // Load Active Customers Count
        ResultSet activeCustomersRS = MySQL2.executeSearch("SELECT COUNT(*) as count FROM `customer` WHERE DATE(registered_date) = CURDATE()");
        if (activeCustomersRS.next()) {
            ActiveCustomersTotalNoLabel.setText(activeCustomersRS.getString("count"));
        } else {
            ActiveCustomersTotalNoLabel.setText("0");
        }
        
        // Load Complete Orders Count
      ResultSet rs = MySQL2.executeSearch(
    "SELECT COUNT(*) AS count FROM employee e " +
    "LEFT JOIN employee_attendance ea ON e.id = ea.employee_id AND ea.date = CURRENT_DATE " +
    "WHERE ea.id IS NULL");

if (rs.next()) {
    TotalOrdersTotalNoLabel.setText(rs.getString("count"));
} else {
    TotalOrdersTotalNoLabel.setText("0");
}

        
        // Load Failed Orders Count
        ResultSet failedOrdersRS = MySQL2.executeSearch(
            "SELECT COUNT(*) as count FROM `employee_attendance` WHERE `date` = CURRENT_DATE");
        if (failedOrdersRS.next()) {
            FailedOrdersTotalNoLabel.setText(failedOrdersRS.getString("count"));
        } else {
            FailedOrdersTotalNoLabel.setText("0");
        }
        
      
       
        
       
        
        // Load Progress Table
        loadProgressTable();
        
    } catch (Exception e) {
        e.printStackTrace();
    }
}


private void loadProgressTable() {
    try {
        // Fetch lists
List<String> customers = getSingleColumnList("SELECT CONCAT(first_name, ' ', last_name) FROM customer WHERE DATE(registered_date) = CURDATE()");
        List<String> presentEmployees = getSingleColumnList("SELECT CONCAT(first_name, ' ', last_name) FROM employee WHERE id IN (SELECT DISTINCT employee_id FROM employee_attendance WHERE date = CURDATE())");
        List<String> absentEmployees = getSingleColumnList("SELECT CONCAT(first_name, ' ', last_name) FROM employee WHERE id NOT IN (SELECT DISTINCT employee_id FROM employee_attendance WHERE date = CURDATE())");
        List<String> foods = getSingleColumnList("SELECT title FROM product");

        // Determine max rows
        int max = Math.max(Math.max(customers.size(), presentEmployees.size()), Math.max(absentEmployees.size(), foods.size()));

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        // Build rows
        for (int i = 0; i < max; i++) {
            Vector<String> row = new Vector<>();
            row.add(i < customers.size() ? customers.get(i) : "");
            row.add(i < presentEmployees.size() ? presentEmployees.get(i) : "");
            row.add(i < absentEmployees.size() ? absentEmployees.get(i) : "");
            row.add(i < foods.size() ? foods.get(i) : "");
            model.addRow(row);
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error loading progress data: " + e.getMessage());
    }
}



    


    
    
    

    
