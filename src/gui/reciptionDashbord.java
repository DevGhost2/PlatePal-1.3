    private List<String> getSingleColumnList(String query) throws Exception {
    List<String> list = new ArrayList<>();
    try {
        ResultSet rs = MySQL2.executeSearch(query);
        while (rs.next()) {
            list.add(rs.getString(1)); // Get the first column value
        }
        rs.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "SQL Error: " + e.getMessage());
    }
    return list;
}


